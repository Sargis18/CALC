a = float(input("Enter number 1: \n"))
sub = input("Enter action: \n")
b = float(input("Enter number 2: \n"))
if sub == "+":
    print("ANSWER")
    print(a + b)
if sub == "-":
    print("ANSWER")
    print(a - b)
if sub == "*":
    print("ANSWER")
    print(a * b)
if sub == "/":
    print("ANSWER")
    print(a / b)
